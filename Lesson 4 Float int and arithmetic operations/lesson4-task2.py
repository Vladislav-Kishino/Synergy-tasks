# Запрашиваем пятизначное число
число = int(input("Введите пятизначное целое число: "))

# Разбиваем число на цифры
десятки_тысяч = число // 10000
тысячи = (число // 1000) % 10
сотни = (число // 100) % 10
десятки = (число // 10) % 10
единицы = число % 10

# Выполняем вычисления
шаг_первый = десятки ** единицы         # десятки в степени единиц
шаг_второй = шаг_первый * сотни         # умножаем на сотни
шаг_третий = десятки_тысяч - тысячи     # разность дес.тыс. и тысяч
результат = шаг_второй / шаг_третий     # финальное деление

# Выводим результат
print(f"Результат вычислений: {результат}")