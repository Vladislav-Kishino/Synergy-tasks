# Задание №3
# Запрашиваем данные для переменных
X = int(input("Введите минимальную сумму инвестиций X: "))
A = int(input("Сколько долларов у Майкла? "))
B = int(input("Сколько долларов у Ивана? "))

# Проверяем условия в порядке приоритета
могут_оба = A >= X and B >= X
майкл_может = A >= X
иван_может = B >= X
могут_вместе = A + B >= X

# Выводим ответ
if могут_оба:
    print(2)
elif майкл_может:
    print("Mike")
elif иван_может:
    print("Ivan")
elif могут_вместе:
    print(1)
else:
    print(0)